
/**
 *  The Class LessThanOrEqualToZeroException Exception class. Exception For Recursion method when parameter is less than or Equal to Zero.
 */
public class LessThanOrEqualToZeroException extends Exception {

	
	/**
	 * Instantiates a new less than or equal to zero exception.
	 */
	public LessThanOrEqualToZeroException() {}

	
	/**
	 * Instantiates a new less than or equal to zero exception.
	 *
	 * @param message the message
	 */
	public LessThanOrEqualToZeroException(String message)
	{
		super(message);
	}
}
